package com.s4n.suCorrientazoADomicilio.SuCorrientazoADomicilio.dto;

public class PosicionDron {
	
	private int 			posicionX;
	private int 			posicionY;
	private PuntoCardinal 	puntoCardinal;
	
	public int getPosicionX() {
		return posicionX;
	}
	public void setPosicionX(int posicionX) {
		this.posicionX = posicionX;
	}
	public int getPosicionY() {
		return posicionY;
	}
	public void setPosicionY(int posicionY) {
		this.posicionY = posicionY;
	}
	public PuntoCardinal getPuntoCardinal() {
		return puntoCardinal;
	}
	public void setPuntoCardinal(PuntoCardinal puntoCardinal) {
		this.puntoCardinal = puntoCardinal;
	}
	
	
}
